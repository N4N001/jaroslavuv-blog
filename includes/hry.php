<p>
Minecraft je sandboxová survival hra, kde hráči musí těžit, získávat suroviny, vyrábět nástroje a zabíjet nepřátelské moby. Hra nemá konec, avšak za dohrání hlavního úkolu hry se mezi hráči považuje zabití draka v End dimenzi. Tam se hráči dostanou pomocí itemů zvaných "Eye of ender". Pro jejich vyrobění musí hráč získat ender perly z moba Enderman a blaez rody z moba Blaze, který se nachází v Nether dimenzi.</br>Byla to hramého dětství a stále je jedan z mých oblíbených.
</p>
<p>
Deus Ex: Mankind Divided je ze série her Deus Ex, které jsou umístěny v budoucnosti. V této budoucnosti se lidé stávají kyborgy díky augmentacím (robotickým končetinám). V Mankind Divided hrajete za Augmentovaného speciálního vojáka, který se v Praze snaží zjistit kdo stojí za bombovým útokem na hlavním nádraží. Nehrál jsem žádné další díly z této série, ale i tak je tahle hra jedna z mých oblíbených.
</p>
<p>
Brawlhalla je platformová soubojová hra na způsob super smash bros. Ve hře je hlavním cílem praštit oponenta tolikrát, že vyletí mimo mapu. Ve hře je spoustu postav a každá postava má dvě zbraně se kterými může oponenta mlátit. Zbraně mají stejné light attacky (levé tlačítko myši) pro všechny postavy co s nimi útočí, ale heavy attacky (pravé tlačítko myši) jsou pro každou postavu jiné.
</p>
